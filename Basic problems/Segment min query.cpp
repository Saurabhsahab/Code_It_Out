VL a(100001,0),b(400005,0);

void bt(ll i,ll s,ll e){

    if(s>e)
    return;

    if(s==e){
    b[i]=a[s];
    return;
    }

    ll m=(s+e)/2;

    bt(2*i,s,m);
    bt((2*i)+1,m+1,e);

    b[i]=min(b[2*i],b[(2*i)+1]);
}

ll qu(ll i,ll l,ll r,ll s,ll e){
    if(s>r||l>e)
    return llfull;

    if(s>=l&&e<=r)
    return b[i];

    ll m=(s+e)/2;

    ll p=qu(2*i,l,r,s,m);
    ll q=qu((2*i)+1,l,r,m+1,e);

    return min(p,q);
}

void uu(ll i,ll y,ll z,ll s,ll e){
    if(s>e)
    return;

    if(s==e){
        a[y]=z;
        b[i]=z;
        return;
    }
    else{

        ll m=(s+e)/2;

        if(y<=m&&y>=s)
        uu(2*i,y,z,s,m);
        else
        uu((2*i)+1,y,z,m+1,e);

        
        
        b[i]=min(b[2*i],b[(2*i)+1]);
    }
}

